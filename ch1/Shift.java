import java.util.*;
public class Shift
{
          public static void main(String args[])
          {
                   int n,x;
                   Scanner sc=new Scanner(System.in);
                   System.out.println("Enter a number:"); 
                   n=sc.nextInt();
                   x=n<<3;
                   System.out.println("After left shifting it will be:"+x); 
           }
}